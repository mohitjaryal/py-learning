# WAP to perform basic arithmetic operations using differnet functions

# sub() - perform addition
def sum(a,b):
    res = a + b
    print('Addition :',res)
    return(res)

# sub() - perform subtraction
def sub(c,d):
    result = c - d
    print('Subtraction :',result)
    return(result)


# mul() - perform multiplication
def mul(e,f):
    ans = e * f
    print('Multiplication :',ans)
    return(ans)


# div() - perform division
def div(g,h):
    final = g / h
    print('Division :',final)
    return(final)

sum(2,3) # Function call 
sub(10,10) # Function call 
div(6,2) # Function call 
mul(2,2)