# WAP to perform basic arithmetic operations using differnet functions

# sub() - perform addition
def sum(a,b):
    res = a + b
    print(res)
    return(res)

# sub() - perform subtraction
def sub(c,d):
    result = c - d
    print(result)
    return(result)


# mul() - perform multiplication
def mul(e,f):
    ans = e * f
    print(ans)
    return(ans)


# div() - perform division
def div(g,h):
    final = g / h
    print(final)
    return(final)

sum(2,3)
sub(10,10)
div(6,2)
mul(2,2)